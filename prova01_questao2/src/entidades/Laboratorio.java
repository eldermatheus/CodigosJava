package entidades;

public class Laboratorio {
	int labNumero;
	int labBloco;

	public Laboratorio(int labNumero, int labBloco) {
		super();
	}

	public int getLabNumero() {
		return labNumero;
	}

	public void setLabNumero(int labNumero) {
		this.labNumero = labNumero;
	}

	public int getLabBloco() {
		return labBloco;
	}

	public void setLabBloco(int labBloco) {
		this.labBloco = labBloco;
	}
}
